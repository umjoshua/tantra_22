import mongoose from "mongoose";

const eventSchema = mongoose.Schema({
    title: String,
    message: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likeCount: {
        type: Number,
        default: 0,
    },
    createdAt: {
        type: Date,
        default: new Date(),
    }
});

const EventDetails = mongoose.model('EventDetails', eventSchema);

export default EventDetails;